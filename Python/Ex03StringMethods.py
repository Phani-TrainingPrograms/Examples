# methods in strings
name = "phani raj"
print(len(name)) # length of the string.
print(name.find("a"))
print(name.capitalize())
print(name.upper())
print(name.lower())# phani
print(name.isdigit)# false
print(name.isalpha()) # checks if it has only alphabets
print(name.replace("a", "aa"))
print(name * 3)
print(f"The name is {name}")